package calculator

class Calculator {
    fun add(i: Int, j: Int): Int {
        return i + j
    }

    fun subtract(i: Int, j: Int): Int {
        return i - j
    }

    fun multiply(i: Int, j: Int): Int {
        return i * j
    }

    fun divide(i: Int, j: Int): Int {
        return i / j
    }
}